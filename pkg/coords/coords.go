// Package coords implements a hex-based coordinate system.
package coords

type Coords struct {
	Col, Row int
}

func (c Coords) Less(d Coords) bool {
	if c.Row < d.Row {
		return true
	} else if c.Row > d.Row {
		return false
	}
	return c.Col < d.Col
}
